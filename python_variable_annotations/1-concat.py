#!/usr/bin/env python3
"""
1-concat.py

This module contains the `concat` function that concat str1 and str2.
"""


def concat(str1: str, str2: str) -> str:
    """
    function concat a type-annotated have two arguments
    str1: type str
    str2: type str
    return concat str1 + str2  type str
    """
    return str1 + str2
